    private void backButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_backButtonActionPerformed
        boolean dispose = false;
        try {
            if(Double.parseDouble(textFieldPayment.getText()) >= 0) { //these codes are used for validation purpose. 
                dispose = true;
                if (textFieldBalance.getText().isEmpty()) {
                    JOptionPane.showMessageDialog(null, "Please click enter to proceed");
                    dispose = false;
                }
                else
                    dispose = true;
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "You can't click back without inputting Payment ");
            dispose = false;
        }

        
        if (dispose) {
            dispose();
        }
           
    

    }//GEN-LAST:event_backButtonActionPerformed

