    private void textFieldDiscountActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_textFieldDiscountActionPerformed
        //validation of the dicount text field, the text field can't be over 100 or below 1... as the dicount is calculated in percentage.
        try {
            if(Double.parseDouble(textFieldDiscount.getText()) >= 1 && Double.parseDouble(textFieldDiscount.getText()) <= 100) {
            try {
            laptopObject.setDiscountRate(Double.parseDouble(textFieldDiscount.getText()));
            } catch (Exception e) {

            }
            laptopObject.Discount();
            textPriceAfterDiscount.setText(laptopObject.getPriceAfterDiscount()+"");
            
            } else {
                JOptionPane.showMessageDialog(null, "You have inputted Discount Percentage Below 1 or Above 100 ");         
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Invalid Input ");           
        }
        
        
    }//GEN-LAST:event_textFieldDiscountActionPerformed

    private void textFieldPaymentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_textFieldPaymentActionPerformed
        try { // try catch block
           //validation so that the the payment is greater or equal to the cost price...
            if((Double.parseDouble(textFieldPayment.getText()) >= Double.parseDouble(textFieldTotalPrice.getText())) || (Double.parseDouble(textFieldPayment.getText()) >= Double.parseDouble(textPriceAfterDiscount.getText())))  { //validation if the user inputted the right input
                try {
                laptopObject.setPayment(Double.parseDouble(textFieldPayment.getText()));          
                } catch (Exception e) {

                }
                laptopObject.Balance();
                textFieldBalance.setText(laptopObject.Balance()+"");
            } else {
                JOptionPane.showMessageDialog(null, "Not enough Money to make the Payment "); //error message if the user inputs payments less than the total.
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Invalid Input ");
        }
        
        
    }//GEN-LAST:event_textFieldPaymentActionPerformed

